import { newSpecPage } from '@stencil/core/testing';
import { BooleanQuestion } from '../boolean-question';

describe('boolean-question', () => {
  it('renders', async () => {
    const page = await newSpecPage({
      components: [BooleanQuestion],
      html: `<boolean-question></boolean-question>`,
    });
    expect(page.root).toEqualHtml(`
      <boolean-question>
        <mock:shadow-root>
          <slot></slot>
        </mock:shadow-root>
      </boolean-question>
    `);
  });
});
